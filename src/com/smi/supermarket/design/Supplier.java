    private void StableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_StableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel mod = (DefaultTableModel) Stable.getModel();
        int indexTable = Stable.getSelectedRow();
        Sid.setText(mod.getValueAt(indexTable, 0).toString());
        Sname.setText(mod.getValueAt(indexTable, 1).toString());
        Sadd.setText(mod.getValueAt(indexTable, 4).toString());
        Sphone.setText(mod.getValueAt(indexTable, 2).toString());
        Semail.setText(mod.getValueAt(indexTable, 3).toString());
        Sother.setText(mod.getValueAt(indexTable, 5).toString());
    }//GEN-LAST:event_StableMouseClicked

