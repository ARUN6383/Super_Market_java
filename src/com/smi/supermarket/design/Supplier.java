    public void insertCode() {
        sp.setSid(Integer.parseInt(Sid.getText()));
        sp.setSname(Sname.getText());
        sp.setSadd(Sadd.getText());
        sp.setSphone(Sphone.getText());
        sp.setSemail(Semail.getText());
        sp.setSother(Sother.getText());

    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        insertCode();

        if (Sid.getText().isEmpty() || Sname.getText().isEmpty() || Sadd.getText().isEmpty() || Sphone.getText().isEmpty() || Semail.getText().isEmpty() || Sother.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Field is Empty");
        } else if (cl.insertSuplier(sp)) {
            JOptionPane.showMessageDialog(null, "Supplier Added successfully");
            supplierTable();
            supplierClear();
        } else {
            JOptionPane.showMessageDialog(null, "Supplier Added failed");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

